$(document).ready(function(){
  $('#send-money').validate({
    ignore: [], 
    highlight: function(element) {
      $(element).closest('.form-group').removeClass('has-success').addClass('has-error'); 
    },
    success: function(element) {
      element.closest('.form-group').removeClass('has-error').addClass('has-success');
      element.remove();
    },
    submitHandler: function(form){
      var ajax_base_url = $('#h_ajax_base_url').val();
      var form = $('#send-money');
      var url = ajax_base_url+form.attr('action');
      $.ajax({
        type: "POST",
        url: url,
        data: form.serialize(), // serializes the form's elements.
        beforeSend: function(){
          $('#result_display').html('');
          $('#send_btn').html('Please Wait <i class="fa fa-spinner fa-spin"></i>');
          $('#send_btn').css('opacity','0.7');
        },
        success: function(result, status, xhr){//alert(result);         
          var res_arr = result.split("|");
          if(res_arr[0]=='success'){
            //$('#result_display').html('<div class="alert alert-danger">'+result+'</div>'); 
            var trans_msg_arr = res_arr[1].split("##");
            var trans_money = trans_msg_arr[0];
            $('#succ_trans_amt').html(trans_money);
            var txn_id = trans_msg_arr[1];
            $('#succ_trans_id').html(txn_id);
            $(".succ_trans_summary").attr("href", ajax_base_url+"transfer_money/details/"+txn_id);
            
            $('#up_bk_step_three').hide();
            $('#up_bk_step_four').show();
            $('#step_bar_three').removeClass('active');
            $('#step_bar_four').addClass('active');
            
          }else{
            $("html, body").animate({ scrollTop: 0 }, "slow");
            $('#result_display').html('<div class="alert alert-danger">'+res_arr[1]+'</div>');
            $('#send_btn').html('Send Money <i class="fas fa-chevron-right"></i>');
            $('#send_btn').css('opacity','1');
          }
        },
        error: function(xhr, status, error_desc){       
          $("html, body").animate({ scrollTop: 0 }, "slow");
          $('#result_display').html('<div class="alert alert-danger">'+error_desc+'</div>');
          $('#send_btn').html('Send Money <i class="fas fa-chevron-right"></i>');
          $('#send_btn').css('opacity','1');
        }
      });   
      return false;
    }
  });
});
